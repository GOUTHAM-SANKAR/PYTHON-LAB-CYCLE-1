'''Read any 2 numbers and find the sum, difference, product, quotient and reminder'''

#Read the first number
A=int(input("Enter the first number:"))

#Read the second number 
B=int(input("Enter the second number:"))

#Add the two numbers
C=A+B

#Subtract two numbers
D=A-B

#Multiply two numbers
E=A*B

#Divide two numbers
F=A/B

#Remainder of two numbers
G=A%B

print("The sum is : ",C,"\n""The difference is :",D,"\n""The product is :",E,"\n","The quotient is :",F,"\n","The remainder is :",G)